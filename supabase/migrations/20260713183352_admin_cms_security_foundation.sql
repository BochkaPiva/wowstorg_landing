create or replace function private.is_active_admin(allowed_roles public.admin_role[])
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.admin_profiles profile
    where profile.id = (select auth.uid())
      and profile.is_active
      and profile.role = any(allowed_roles)
  );
$$;

revoke all on function private.is_active_admin(public.admin_role[]) from public;
grant usage on schema private to authenticated;
grant execute on function private.is_active_admin(public.admin_role[]) to authenticated;

