create schema if not exists private;
revoke all on schema private from public;

create type public.admin_role as enum ('owner', 'admin', 'editor', 'viewer');
create type public.lead_status as enum ('new', 'contacted', 'qualified', 'rejected', 'converted');

create table public.admin_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  role public.admin_role not null default 'viewer',
  is_active boolean not null default true,
  display_name text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.content_documents (
  id uuid primary key default gen_random_uuid(),
  content_key text not null unique check (content_key ~ '^[a-z0-9][a-z0-9_-]{1,63}$'),
  draft_payload jsonb not null default '{}'::jsonb
    check (jsonb_typeof(draft_payload) = 'object' and pg_column_size(draft_payload) <= 1048576),
  version bigint not null default 1 check (version > 0),
  updated_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.published_content (
  content_key text primary key check (content_key ~ '^[a-z0-9][a-z0-9_-]{1,63}$'),
  payload jsonb not null
    check (jsonb_typeof(payload) = 'object' and pg_column_size(payload) <= 1048576),
  version bigint not null check (version > 0),
  published_at timestamptz not null default now()
);

create table public.content_revisions (
  id bigint generated by default as identity primary key,
  document_id uuid not null references public.content_documents(id) on delete cascade,
  version bigint not null check (version > 0),
  snapshot jsonb not null,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  unique (document_id, version)
);

create table public.admin_audit_log (
  id bigint generated by default as identity primary key,
  actor_id uuid references auth.users(id) on delete set null,
  action text not null check (length(action) between 3 and 80),
  entity_type text not null check (length(entity_type) between 2 and 80),
  entity_id text,
  metadata jsonb not null default '{}'::jsonb
    check (jsonb_typeof(metadata) = 'object' and pg_column_size(metadata) <= 65536),
  created_at timestamptz not null default now()
);

create table public.landing_leads (
  id uuid primary key default gen_random_uuid(),
  status public.lead_status not null default 'new',
  source text not null default 'landing' check (source in ('landing', 'manual')),
  name text not null check (length(name) between 1 and 160),
  contact text not null check (length(contact) between 3 and 320),
  event_type text,
  guest_range text,
  event_date date,
  message text check (message is null or length(message) <= 5000),
  catalog_selection_ids text[] not null default '{}'
    check (cardinality(catalog_selection_ids) <= 200),
  consent_version text not null,
  consented_at timestamptz not null,
  converted_project_id text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index landing_leads_status_created_idx on public.landing_leads (status, created_at desc);
create index content_revisions_document_created_idx on public.content_revisions (document_id, created_at desc);
create index admin_audit_log_actor_created_idx on public.admin_audit_log (actor_id, created_at desc);

create or replace function private.set_updated_at()
returns trigger
language plpgsql
security invoker
set search_path = ''
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

